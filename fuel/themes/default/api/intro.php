<h3>The FuelPHP API</h3>
<p>Use the dropdown box to select the version of FuelPHP you are using.</p>
<p>Use the navigation menu on the left to search for functions or classes.</p>
<p>Click on the package name to reveal the list of functions or classes in that package, and click on one to view the API documentation.</p>
<p>When viewing the documentation, the navigation menu will highlight all functions and classes defined in the file you have selected.</p>
